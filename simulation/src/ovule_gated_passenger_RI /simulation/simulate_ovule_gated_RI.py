@dataclass
class Scenario:
    n_tips: int = 80
    alpha: float = 0.6
    psi: float = 0.02
    pollen_c: float = 300.0
    pollen_noise: float = 0.35
    architecture: str = "pleiotropic"  # independent, linked, pleiotropic
    recombination: float = 0.01
    pleiotropy: float = 0.75
    state_transition_rate: float = 2.0
    many_log_ovules: float = math.log(200.0)
    few_log_ovules: float = math.log(25.0)
    ovule_residual_sd: float = 0.25
    ovule_brownian_sd: float = 0.20
    filter_noise_sd: float = 0.08
    drift_mismatch_rate: float = 0.30
    selected_mismatch_scale: float = 0.25
    sweep_base: float = 0.05
    sweep_scale: float = 2.50
    post_lambda: float = 3.0
    post_gamma: float = 1.5
    post_h: float = 0.25
    seed_denominator_multiplier: float = 10.0
    beta_binomial_rho: float = 0.05
    post_n: int = 50


def simulate_one_replicate(
    scenario: Scenario,
    rng: np.random.Generator,
    replicate: int = 0,
) -> Tuple[pd.DataFrame, pd.DataFrame]:
    """Simulate one tree, lineage table, and pair table."""

    nodes, root_id, tip_ids = simulate_coalescent_tree(scenario.n_tips, rng)
    div_df = pairwise_mrca_heights(nodes, tip_ids)

    states = simulate_two_state_trait(
        nodes, root_id, tip_ids, rng,
        q=scenario.state_transition_rate,
        root_state=0,
    )
    brownian = simulate_brownian_trait(
        nodes, root_id, tip_ids, rng,
        root_value=0.0,
        sigma=scenario.ovule_brownian_sd,
    )

    records = []
    for tip in tip_ids:
        state = states[tip]
        state_name = "few" if state == 1 else "many"
        theta = scenario.few_log_ovules if state == 1 else scenario.many_log_ovules
        log_ovules = theta + brownian[tip] + rng.normal(0.0, scenario.ovule_residual_sd)
        ovules = max(2.0, float(np.exp(log_ovules)))
        pollen_production = scenario.pollen_c * (ovules ** scenario.alpha) * math.exp(rng.normal(0.0, scenario.pollen_noise))
        pollen_eff = scenario.psi * pollen_production
        K = pollen_eff / ovules
        Sz = truncation_selection(K, sigma_z=1.0)
        beta_z = Sz  # sigma_z = 1, so beta_z = S_z
        sweep_rate = scenario.sweep_base + scenario.sweep_scale * max(beta_z, 0.0)
        sweep_count = rng.poisson(max(sweep_rate, 1e-9))
        omega = competition_opportunity(pollen_eff, ovules, rng)
        filter_F = maternal_filter_from_K(np.array([K]))[0] + rng.normal(0.0, scenario.filter_noise_sd)
        filter_F = max(0.0, filter_F)

        records.append({
            "replicate": replicate,
            "species": f"sp_{tip:03d}",
            "tip_id": tip,
            "state": state_name,
            "ovules": ovules,
            "log_ovules": math.log(ovules),
            "pollen_production": pollen_production,
            "pollen_eff": pollen_eff,
            "psi": scenario.psi,
            "alpha": scenario.alpha,
            "K": K,
            "logK": math.log(max(K, 1e-12)),
            "omega": omega,
            "pollen_selection_Sz": Sz,
            "sweep_count": sweep_count,
            "filter_F": filter_F,
        })

    lineage_df = pd.DataFrame.from_records(records)
    lookup = lineage_df.set_index("tip_id")

    pair_records = []
    for row in div_df.itertuples(index=False):
        i = int(row.i)
        j = int(row.j)
        ti = lookup.loc[i]
        tj = lookup.loc[j]
        T = float(row.divergence_time)

        base_mismatch = scenario.drift_mismatch_rate * T + abs(rng.normal(0.0, 0.03))
        mean_sweeps = 0.5 * (float(ti.sweep_count) + float(tj.sweep_count))
        delta_logK = float(ti.logK - tj.logK)
        mean_selection = 0.5 * (float(ti.pollen_selection_Sz) + float(tj.pollen_selection_Sz))

        if scenario.architecture == "independent":
            arch_coupling = 0.04
        elif scenario.architecture == "linked":
            H = mean_selection / (mean_selection + scenario.recombination + 1e-9)
            arch_coupling = 0.45 * H
        elif scenario.architecture == "pleiotropic":
            arch_coupling = 0.70 * scenario.pleiotropy
        else:
            raise ValueError(f"Unknown architecture: {scenario.architecture}")

        selected_mismatch = scenario.selected_mismatch_scale * arch_coupling * (
            mean_sweeps + 0.5 * abs(delta_logK)
        )
        D = max(0.0, base_mismatch + selected_mismatch)

        F_i = float(ti.filter_F)
        F_j = float(tj.filter_F)
        Rpre_i_from_j = 1.0 - math.exp(-F_i * D)
        Rpre_j_from_i = 1.0 - math.exp(-F_j * D)
        asym = Rpre_i_from_j - Rpre_j_from_i

        B = rng.poisson(max(scenario.post_lambda * (T ** scenario.post_gamma), 0.0))
        Rpost = 1.0 - math.exp(-scenario.post_h * B)

        # Observed seed counts. Denominator scales with maternal ovules and number
        # of flowers crossed, represented by seed_denominator_multiplier.
        N_i = max(1, int(round(float(ti.ovules) * scenario.seed_denominator_multiplier)))
        N_j = max(1, int(round(float(tj.ovules) * scenario.seed_denominator_multiplier)))
        seed_prob_i = np.clip(1.0 - Rpre_i_from_j, 1e-6, 1.0 - 1e-6)
        seed_prob_j = np.clip(1.0 - Rpre_j_from_i, 1e-6, 1.0 - 1e-6)
        seeds_i = beta_binomial_rng(rng, np.array([N_i]), np.array([seed_prob_i]), scenario.beta_binomial_rho)[0]
        seeds_j = beta_binomial_rng(rng, np.array([N_j]), np.array([seed_prob_j]), scenario.beta_binomial_rho)[0]
        post_success = rng.binomial(scenario.post_n, np.clip(1.0 - Rpost, 1e-6, 1.0 - 1e-6))

        pair_records.append({
            "replicate": replicate,
            "species_i": ti.species,
            "species_j": tj.species,
            "tip_i": i,
            "tip_j": j,
            "state_i": ti.state,
            "state_j": tj.state,
            "divergence_time": T,
            "ovules_i": ti.ovules,
            "ovules_j": tj.ovules,
            "logO_i": ti.log_ovules,
            "logO_j": tj.log_ovules,
            "K_i": ti.K,
            "K_j": tj.K,
            "logK_i": ti.logK,
            "logK_j": tj.logK,
            "delta_logK_i_minus_j": delta_logK,
            "delta_logO_j_minus_i": float(tj.log_ovules - ti.log_ovules),
            "filter_i": F_i,
            "filter_j": F_j,
            "mismatch_D": D,
            "Rpre_i_from_j": Rpre_i_from_j,
            "Rpre_j_from_i": Rpre_j_from_i,
            "asym_i_minus_j": asym,
            "Rpost": Rpost,
            "BDM_count": B,
            "N_seed_i_from_j": N_i,
            "N_seed_j_from_i": N_j,
            "seeds_i_from_j": int(seeds_i),
            "seeds_j_from_i": int(seeds_j),
            "observed_Rpre_i_from_j": 1.0 - seeds_i / N_i,
            "observed_Rpre_j_from_i": 1.0 - seeds_j / N_j,
            "observed_asym_i_minus_j": (1.0 - seeds_i / N_i) - (1.0 - seeds_j / N_j),
            "N_post": scenario.post_n,
            "post_success": int(post_success),
            "observed_Rpost": 1.0 - post_success / scenario.post_n,
        })

    pair_df = pd.DataFrame.from_records(pair_records)
    return lineage_df, pair_df


