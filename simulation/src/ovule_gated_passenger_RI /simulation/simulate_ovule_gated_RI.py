def truncation_selection(K: np.ndarray | float, sigma_z: float = 1.0) -> np.ndarray | float:
    """Selection differential among successful pollen grains.

    If K <= 1, no truncation component is imposed. If K > 1, the successful
    pollen grains are the top q = 1/K fraction under a normal performance trait.
    """

    K_arr = np.asarray(K, dtype=float)
    out = np.zeros_like(K_arr)
    mask = K_arr > 1.0
    if np.any(mask):
        q = 1.0 / K_arr[mask]
        # Avoid infinite quantiles at numerical extremes.
        q = np.clip(q, 1e-9, 1.0 - 1e-9)
        t = norm.ppf(1.0 - q)
        out[mask] = sigma_z * norm.pdf(t) / q
    if np.ndim(K) == 0:
        return float(out)
    return out


def competition_opportunity(mean_pollen: float, ovules: float, rng: np.random.Generator, theta: float = 5.0, n_flowers: int = 500) -> float:
    """Estimate Omega = E[((P - O)+) / P] under negative-binomial pollen loads."""

    if mean_pollen <= 0 or ovules <= 0:
        return 0.0
    # Negative binomial parameterisation: variance = mu + mu^2/theta.
    # numpy uses n and p, where mean = n(1-p)/p.
    n = theta
    p = theta / (theta + mean_pollen)
    pollen = rng.negative_binomial(n=n, p=p, size=n_flowers).astype(float)
    with np.errstate(divide="ignore", invalid="ignore"):
        omega = np.where(pollen > 0, np.maximum(pollen - ovules, 0.0) / pollen, 0.0)
    return float(np.mean(omega))


def maternal_filter_from_K(
    K: np.ndarray,
    F_min: float = 0.05,
    F_max: float = 2.0,
    half_sat: float = 1.0,
) -> np.ndarray:
    """A smooth monotone approximation to F*(K).

    The LaTeX note gives a mechanistic optimisation model. For simulation we
    use a saturating function that preserves the qualitative result dF*/dK > 0.
    """

    x = np.log1p(np.maximum(K, 0.0))
    return F_min + F_max * x / (x + half_sat)


def beta_binomial_rng(rng: np.random.Generator, n: np.ndarray, p: np.ndarray, rho: float = 0.05) -> np.ndarray:
    """Generate beta-binomial counts with intraclass correlation rho.

    rho close to 0 approaches a binomial. rho > 0 adds overdispersion.
    """

    n = np.asarray(n, dtype=int)
    p = np.clip(np.asarray(p, dtype=float), 1e-6, 1.0 - 1e-6)
    rho = float(np.clip(rho, 1e-6, 0.95))
    phi = (1.0 - rho) / rho
    alpha = p * phi
    beta = (1.0 - p) * phi
    p_draw = rng.beta(alpha, beta)
    return rng.binomial(n, p_draw)


def ols_fit(y: np.ndarray, X: np.ndarray, names: List[str]) -> Dict[str, float]:
    """Small OLS helper returning coefficients and R^2."""

    ok = np.isfinite(y) & np.all(np.isfinite(X), axis=1)
    if ok.sum() <= X.shape[1]:
        return {f"beta_{name}": np.nan for name in names} | {"r2": np.nan, "n": int(ok.sum())}
    y0 = y[ok]
    X0 = X[ok]
    coef, *_ = np.linalg.lstsq(X0, y0, rcond=None)
    fitted = X0 @ coef
    ss_res = float(np.sum((y0 - fitted) ** 2))
    ss_tot = float(np.sum((y0 - np.mean(y0)) ** 2))
    r2 = 1.0 - ss_res / ss_tot if ss_tot > 0 else np.nan
    out = {f"beta_{name}": float(c) for name, c in zip(names, coef)}
    out["r2"] = r2
    out["n"] = int(ok.sum())
    return out


