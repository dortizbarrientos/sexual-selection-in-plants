@dataclass
class Node:
    """A node in a simple ultrametric tree.

    We build the tree backwards from present-day tips using a coalescent-like
    process. Tip height is 0; the root has the largest height. Branch length
    from parent to child is parent.height - child.height.
    """

    node_id: int
    height: float
    children: List[int] = field(default_factory=list)
    parent: Optional[int] = None


def simulate_coalescent_tree(n_tips: int, rng: np.random.Generator) -> Tuple[Dict[int, Node], int, List[int]]:
    """Simulate a simple ultrametric binary tree.

    The topology is random and branch lengths are generated by waiting times
    proportional to the number of possible coalescent pairs. Heights are scaled
    so that the root height is one.
    """

    if n_tips < 2:
        raise ValueError("n_tips must be at least 2")

    nodes: Dict[int, Node] = {i: Node(node_id=i, height=0.0) for i in range(n_tips)}
    active = list(range(n_tips))
    next_id = n_tips
    current_height = 0.0

    while len(active) > 1:
        k = len(active)
        rate = k * (k - 1) / 2.0
        current_height += rng.exponential(1.0 / rate)
        a, b = rng.choice(active, size=2, replace=False)
        parent = Node(node_id=next_id, height=current_height, children=[int(a), int(b)])
        nodes[next_id] = parent
        nodes[int(a)].parent = next_id
        nodes[int(b)].parent = next_id
        active = [x for x in active if x not in (a, b)]
        active.append(next_id)
        next_id += 1

    root_id = active[0]
    root_height = nodes[root_id].height
    if root_height <= 0:
        raise RuntimeError("Tree root height was zero; this should not happen.")

    # Normalise heights to root height = 1.
    for node in nodes.values():
        node.height /= root_height

    tip_ids = list(range(n_tips))
    return nodes, root_id, tip_ids


def ancestors(nodes: Dict[int, Node], tip_id: int) -> Dict[int, float]:
    """Return ancestor nodes and their heights for one tip, including the tip."""

    out: Dict[int, float] = {}
    current = tip_id
    while current is not None:
        out[current] = nodes[current].height
        current = nodes[current].parent
    return out


def pairwise_mrca_heights(nodes: Dict[int, Node], tip_ids: List[int]) -> pd.DataFrame:
    """Calculate pairwise MRCA heights for all unordered tip pairs."""

    ancestor_maps = {tip: ancestors(nodes, tip) for tip in tip_ids}
    records = []
    for i, j in itertools.combinations(tip_ids, 2):
        common = set(ancestor_maps[i]).intersection(ancestor_maps[j])
        # In this backward-time representation, the MRCA is the common ancestor
        # with the smallest height greater than zero that is shared by the pair.
        # Because root height is largest and tips are zero, min positive common
        # is the most recent shared ancestor.
        mrca_height = min(ancestor_maps[i][a] for a in common if ancestor_maps[i][a] > 0)
        records.append({"i": i, "j": j, "divergence_time": mrca_height})
    return pd.DataFrame.from_records(records)


def simulate_brownian_trait(
    nodes: Dict[int, Node],
    root_id: int,
    tip_ids: List[int],
    rng: np.random.Generator,
    root_value: float = 0.0,
    sigma: float = 1.0,
) -> Dict[int, float]:
    """Simulate a Brownian trait down the tree and return tip values."""

    values: Dict[int, float] = {root_id: root_value}
    stack = [root_id]
    while stack:
        parent = stack.pop()
        for child in nodes[parent].children:
            bl = nodes[parent].height - nodes[child].height
            values[child] = values[parent] + rng.normal(0.0, sigma * math.sqrt(max(bl, 0.0)))
            stack.append(child)
    return {tip: values[tip] for tip in tip_ids}


def simulate_two_state_trait(
    nodes: Dict[int, Node],
    root_id: int,
    tip_ids: List[int],
    rng: np.random.Generator,
    q: float = 2.0,
    root_state: int = 0,
) -> Dict[int, int]:
    """Simulate a symmetric two-state trait on the tree.

    State 0 = many ovules; state 1 = few ovules.
    For a symmetric two-state continuous-time Markov chain, the probability of
    a state change over branch length t is 0.5 * (1 - exp(-2 q t)).
    """

    states: Dict[int, int] = {root_id: root_state}
    stack = [root_id]
    while stack:
        parent = stack.pop()
        for child in nodes[parent].children:
            bl = nodes[parent].height - nodes[child].height
            p_switch = 0.5 * (1.0 - math.exp(-2.0 * q * max(bl, 0.0)))
            state = states[parent]
            if rng.random() < p_switch:
                state = 1 - state
            states[child] = state
            stack.append(child)
    return {tip: states[tip] for tip in tip_ids}


