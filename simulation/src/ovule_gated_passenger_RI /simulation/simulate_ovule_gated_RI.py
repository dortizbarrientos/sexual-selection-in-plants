#!/usr/bin/env python3
"""
Ovule-gated pollen competition and passenger reproductive isolation.

This script is a deliberately transparent simulation sandbox. It is not meant
as a final inference engine. It asks when a simple mechanism is recoverable:

    ovule number -> K = effective pollen load / ovule number
    -> pollen competition -> maternal filtering -> directional prezygotic RI

The script can:
  1. simulate an ultrametric phylogeny using a simple coalescent tree,
  2. simulate few- and many-ovule states on that tree,
  3. generate effective pollen load under a scaling model,
  4. compute K, pollen competition, sweeps, maternal filters,
  5. simulate prezygotic and postzygotic reproductive isolation,
  6. impose different empirical data structures,
  7. summarise whether reciprocal asymmetry recovers delta log(K).

Dependencies: numpy, pandas, scipy, matplotlib.

Example:
    python simulate_ovule_gated_RI.py --outdir ../results_demo --quick

Author: Daniel Ortiz-Barrientos project sandbox; generated with CG.
"""

