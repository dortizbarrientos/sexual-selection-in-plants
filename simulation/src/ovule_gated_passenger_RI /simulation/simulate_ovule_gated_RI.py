def impose_data_structure(pair_df: pd.DataFrame, structure: str, rng: np.random.Generator, sparse_fraction: float = 0.25) -> pd.DataFrame:
    """Return a modified pair table representing a realistic data structure."""

    df = pair_df.copy()

    if structure == "dense_reciprocal":
        return df

    if structure == "sparse_reciprocal":
        keep = rng.random(len(df)) < sparse_fraction
        return df.loc[keep].reset_index(drop=True)

    if structure == "pi_pairs":
        # A simple greedy approximation: choose pairs without species reuse.
        shuffled = df.sample(frac=1.0, random_state=int(rng.integers(0, 2**31 - 1))).reset_index(drop=True)
        used = set()
        keep_idx = []
        for idx, row in shuffled.iterrows():
            if row.tip_i not in used and row.tip_j not in used:
                keep_idx.append(idx)
                used.add(row.tip_i)
                used.add(row.tip_j)
        return shuffled.loc[keep_idx].reset_index(drop=True)

    if structure == "one_direction":
        # Keep only one direction by deleting observed reciprocal asymmetry.
        # True asymmetry is retained for diagnostics, but observed asymmetry is set missing.
        maternal_i = rng.random(len(df)) < 0.5
        df["observed_Rpre_one_direction"] = np.where(
            maternal_i,
            df["observed_Rpre_i_from_j"],
            df["observed_Rpre_j_from_i"],
        )
        df["maternal_logK_one_direction"] = np.where(maternal_i, df["logK_i"], df["logK_j"])
        df["observed_asym_i_minus_j"] = np.nan
        return df

    if structure == "summary_only":
        # Keep summary RI but lose denominators and raw seed counts.
        drop_cols = [
            "N_seed_i_from_j", "N_seed_j_from_i",
            "seeds_i_from_j", "seeds_j_from_i",
            "N_post", "post_success",
        ]
        return df.drop(columns=[c for c in drop_cols if c in df.columns])

    raise ValueError(f"Unknown data structure: {structure}")


def summarise_recovery(pair_df: pd.DataFrame, structure: str) -> Dict[str, float]:
    """Summarise whether asymmetry recovers delta log(K)."""

    out: Dict[str, float] = {"data_structure": structure}

    if structure == "one_direction":
        y = pair_df["observed_Rpre_one_direction"].to_numpy(dtype=float)
        X = np.column_stack([
            np.ones(len(pair_df)),
            pair_df["maternal_logK_one_direction"].to_numpy(dtype=float),
            pair_df["divergence_time"].to_numpy(dtype=float),
        ])
        res = ols_fit(y, X, ["intercept", "maternal_logK", "divergence_time"])
        out.update(res)
        return out

    y = pair_df["observed_asym_i_minus_j"].to_numpy(dtype=float)
    X = np.column_stack([
        np.ones(len(pair_df)),
        pair_df["delta_logK_i_minus_j"].to_numpy(dtype=float),
        pair_df["divergence_time"].to_numpy(dtype=float),
    ])
    res_k = ols_fit(y, X, ["intercept", "delta_logK", "divergence_time"])
    out.update({f"asym_model_{k}": v for k, v in res_k.items()})

    # Proxy model using ovules only: asymmetry should increase with logO_j - logO_i
    # under alpha < 1 because low O_i implies high K_i.
    Xo = np.column_stack([
        np.ones(len(pair_df)),
        pair_df["delta_logO_j_minus_i"].to_numpy(dtype=float),
        pair_df["divergence_time"].to_numpy(dtype=float),
    ])
    res_o = ols_fit(y, Xo, ["intercept", "delta_logO_proxy", "divergence_time"])
    out.update({f"ovule_proxy_{k}": v for k, v in res_o.items()})

    # Postzygotic contrast: observed Rpost should track divergence time.
    yp = pair_df["observed_Rpost"].to_numpy(dtype=float)
    Xp = np.column_stack([
        np.ones(len(pair_df)),
        pair_df["divergence_time"].to_numpy(dtype=float),
        np.abs(pair_df["delta_logK_i_minus_j"].to_numpy(dtype=float)),
    ])
    res_p = ols_fit(yp, Xp, ["intercept", "divergence_time", "abs_delta_logK"])
    out.update({f"post_model_{k}": v for k, v in res_p.items()})

    return out


def plot_demo(lineage_df: pd.DataFrame, pair_df: pd.DataFrame, outdir: Path) -> None:
    """Write a few simple diagnostic plots."""

    outdir.mkdir(parents=True, exist_ok=True)

    fig = plt.figure(figsize=(6, 4.5))
    ax = fig.add_subplot(111)
    ax.scatter(lineage_df["ovules"], lineage_df["K"], alpha=0.75)
    ax.axhline(1.0, linestyle="--", linewidth=1)
    ax.set_xscale("log")
    ax.set_yscale("log")
    ax.set_xlabel("Ovules per ovary")
    ax.set_ylabel("K = effective pollen load / ovules")
    ax.set_title("Competition state across simulated lineages")
    fig.tight_layout()
    fig.savefig(outdir / "fig_K_vs_ovules.png", dpi=300)
    plt.close(fig)

    fig = plt.figure(figsize=(6, 4.5))
    ax = fig.add_subplot(111)
    ax.scatter(pair_df["delta_logK_i_minus_j"], pair_df["observed_asym_i_minus_j"], alpha=0.35)
    ax.axhline(0.0, linestyle="--", linewidth=1)
    ax.axvline(0.0, linestyle="--", linewidth=1)
    ax.set_xlabel("log(K_i) - log(K_j)")
    ax.set_ylabel("Observed asymmetry: RI(i<-j) - RI(j<-i)")
    ax.set_title("Directional prezygotic RI tracks maternal competition state")
    fig.tight_layout()
    fig.savefig(outdir / "fig_asymmetry_vs_delta_logK.png", dpi=300)
    plt.close(fig)

    fig = plt.figure(figsize=(6, 4.5))
    ax = fig.add_subplot(111)
    ax.scatter(pair_df["divergence_time"], pair_df["observed_Rpost"], alpha=0.35)
    ax.set_xlabel("Divergence time on simulated tree")
    ax.set_ylabel("Observed postzygotic RI")
    ax.set_title("Postzygotic RI as a divergence-time contrast")
    fig.tight_layout()
    fig.savefig(outdir / "fig_postRI_vs_divergence.png", dpi=300)
    plt.close(fig)


def run_grid(args: argparse.Namespace) -> None:
    outdir = Path(args.outdir)
    outdir.mkdir(parents=True, exist_ok=True)
    rng_master = np.random.default_rng(args.seed)

    if args.quick:
        alphas = [0.6, 1.0, 1.25]
        psis = [0.005, 0.02, 0.05]
        architectures = ["independent", "linked", "pleiotropic"]
        structures = ["dense_reciprocal", "sparse_reciprocal", "pi_pairs", "one_direction", "summary_only"]
        n_reps = min(args.n_reps, 8)
    else:
        alphas = args.alphas
        psis = args.psis
        architectures = args.architectures
        structures = args.data_structures
        n_reps = args.n_reps

    all_summaries: List[Dict[str, float]] = []
    saved_demo = False

    for alpha, psi, arch, rep in itertools.product(alphas, psis, architectures, range(n_reps)):
        scenario = Scenario(
            n_tips=args.n_tips,
            alpha=alpha,
            psi=psi,
            pollen_c=args.pollen_c,
            architecture=arch,
            recombination=args.recombination,
            pleiotropy=args.pleiotropy,
        )
        rng = np.random.default_rng(int(rng_master.integers(0, 2**31 - 1)))
        lineage_df, pair_df = simulate_one_replicate(scenario, rng, replicate=rep)

        if not saved_demo and arch == architectures[-1] and alpha == alphas[0] and abs(psi - 0.02) < 1e-12:
            lineage_df.to_csv(outdir / "demo_lineages.csv", index=False)
            pair_df.to_csv(outdir / "demo_pairs_full.csv", index=False)
            plot_demo(lineage_df, pair_df, outdir)
            saved_demo = True

        for structure in structures:
            degraded = impose_data_structure(pair_df, structure, rng)
            summary = summarise_recovery(degraded, structure)
            summary.update({
                "replicate": rep,
                "alpha": alpha,
                "psi": psi,
                "architecture": arch,
                "n_tips": args.n_tips,
                "mean_K": float(lineage_df["K"].mean()),
                "median_K": float(lineage_df["K"].median()),
                "prop_K_gt_1": float(np.mean(lineage_df["K"] > 1.0)),
                "mean_omega": float(lineage_df["omega"].mean()),
                "mean_filter_F": float(lineage_df["filter_F"].mean()),
                "mean_sweeps": float(lineage_df["sweep_count"].mean()),
                "n_pairs_used": int(len(degraded)),
            })
            all_summaries.append(summary)

    summary_df = pd.DataFrame(all_summaries)
    summary_df.to_csv(outdir / "scenario_summary_by_replicate.csv", index=False)

    # Aggregate numerical columns.
    group_cols = ["alpha", "psi", "architecture", "data_structure"]
    numeric_cols = [c for c in summary_df.columns if c not in group_cols and pd.api.types.is_numeric_dtype(summary_df[c])]
    aggregate = summary_df.groupby(group_cols)[numeric_cols].agg(["mean", "std"]).reset_index()
    aggregate.columns = ["_".join([str(x) for x in col if x]) for col in aggregate.columns.to_flat_index()]
    aggregate.to_csv(outdir / "scenario_summary_aggregated.csv", index=False)

    print(f"Wrote outputs to: {outdir}")
    print("Key files:")
    print(f"  {outdir / 'demo_lineages.csv'}")
    print(f"  {outdir / 'demo_pairs_full.csv'}")
    print(f"  {outdir / 'scenario_summary_by_replicate.csv'}")
    print(f"  {outdir / 'scenario_summary_aggregated.csv'}")


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Simulate the ovule-gated passenger RI model.")
    parser.add_argument("--outdir", default="results_demo", help="Output directory")
    parser.add_argument("--seed", type=int, default=12345, help="Random seed")
    parser.add_argument("--n_tips", type=int, default=80, help="Number of species tips")
    parser.add_argument("--n_reps", type=int, default=20, help="Replicates per parameter combination")
    parser.add_argument("--quick", action="store_true", help="Run a small demonstration grid")
    parser.add_argument("--pollen_c", type=float, default=300.0, help="Scaling constant for pollen production")
    parser.add_argument("--recombination", type=float, default=0.01, help="Recombination parameter for linked architecture")
    parser.add_argument("--pleiotropy", type=float, default=0.75, help="Pleiotropic coupling parameter")
    parser.add_argument("--alphas", type=float, nargs="+", default=[0.25, 0.5, 0.75, 1.0, 1.25])
    parser.add_argument("--psis", type=float, nargs="+", default=[0.001, 0.003, 0.01, 0.02, 0.05, 0.10])
    parser.add_argument("--architectures", nargs="+", default=["independent", "linked", "pleiotropic"])
    parser.add_argument(
        "--data_structures",
        nargs="+",
        default=["dense_reciprocal", "sparse_reciprocal", "pi_pairs", "one_direction", "summary_only"],
    )
    return parser.parse_args()


if __name__ == "__main__":
    run_grid(parse_args())
